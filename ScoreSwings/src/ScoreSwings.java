    private void sUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sUpdateBtnActionPerformed
        if(clickedStudent == null)  {
            return;
        }
        clickedStudent.setName(this.sNameTxt.getText());
        clickedStudent.setKor(Integer.parseInt(this.sScoreTxt.getText()));
        
        showTotalList();
        clickedStudent = null;
    }//GEN-LAST:event_sUpdateBtnActionPerformed

    private void sDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sDeleteBtnActionPerformed
        if(clickedStudent == null)    {
            return;
        }
        totalList.remove(clickedStudent);
        showTotalList();
        clickedStudent = null;
    }//GEN-LAST:event_sDeleteBtnActionPerformed
   
    public void insertCompenent(String line)    {
            StudentSwings2 obj2 = new StudentSwings2(line);
            totalList.add(obj2);
            showTotalList();
        }
    
    private void sloadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sloadBtnActionPerformed
        FileDialog fileDialogOpen = new FileDialog(this, "파일 열기", FileDialog.LOAD);
        fileDialogOpen.setVisible(true);
        String filePath = fileDialogOpen.getDirectory() + fileDialogOpen.getFile();
        sNameTxt.setText(filePath);
        
        try {
            Scanner scn = new Scanner(new File(filePath));
            String line = "";
            while (true)    {
                try {
                    line = scn.nextLine();
                    insertCompenent(line);
                }   catch(Exception e)  {
                    System.out.println("Line : " + line);
                    System.out.println(e.getMessage());
                    e.printStackTrace();
                    break;
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_sloadBtnActionPerformed
    
    Vector searchList = new Vector();
    private void sSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sSearchBtnActionPerformed
        searchList.clear();
        String str = this.sNameTxt.getText();
        
        for (int i = 0; i < totalList.size(); i++) {
            StudentSwings2 student = (StudentSwings2)totalList.get(i);
            if(student.getName().contains(str))   {
                searchList.add(student);
            }
        }
        
        showList = searchList;
        showList();
    }//GEN-LAST:event_sSearchBtnActionPerformed

