            this.jResultPnl.add(obj);
//          @Override
            obj.addActionListener(new java.awt.event.ActionListener() {
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    SButton obj = (SButton)evt.getSource();
                    StudentSwings2 student = obj.getstudent();
                    sNameTxt.setText(student.getName());
                    sScoreTxt.setText("" + student.getKor());
                    clickedStudent = student;
            }
        });
