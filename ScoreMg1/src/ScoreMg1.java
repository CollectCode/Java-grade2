    private void jInsertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsertBtnActionPerformed
        StudentVO obj = new StudentVO(this.jNameTxt.getText() +
                " " + this.jKorTxt.getText() +
                " " + this.jEngTxt.getText() +
                " " + this.jMathTxt.getText()
                );
        totalList.add(obj);
        showList();
    }//GEN-LAST:event_jInsertBtnActionPerformed

    private void jUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdateBtnActionPerformed
        if (clickedStudent == null) {
            return;
        }
        
        clickedStudent.setName(this.jNameTxt.getText());
        clickedStudent.setKor(Integer.parseInt(this.jKorTxt.getText()));
        clickedStudent.setEng(Integer.parseInt(this.jEngTxt.getText()));
        clickedStudent.setMath(Integer.parseInt(this.jMathTxt.getText()));
        clickedStudent.setAvg(clickedStudent.getAvg());
        clickedStudent.setRank(Integer.parseInt(this.jRankTxt.getText()));
        jAvgTxt.setText("" + clickedStudent.getAvg());
        
        showList();
    }//GEN-LAST:event_jUpdateBtnActionPerformed

    private void jDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteBtnActionPerformed
        if (clickedStudent == null) {
            return;
        }
        
        totalList.remove(clickedStudent);
        showList();
    }//GEN-LAST:event_jDeleteBtnActionPerformed

    private void jSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchBtnActionPerformed
        searchList.clear();
        
        for (int i = 0; i < totalList.size(); i++)  {
            StudentVO temp = totalList.get(i);
            if (temp.getName().contains(this.jNameTxt.getText()))  {
                searchList.add(temp);
            } else if (temp.getName().equals("\\s+")) {
                showList();
            }
        }  
        
        showSearchList();
    }//GEN-LAST:event_jSearchBtnActionPerformed

    private void jFirstBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFirstBtnActionPerformed
        if (currentPageNum >= 1)    {
            currentPageNum = 1;
        }
        showList();
    }//GEN-LAST:event_jFirstBtnActionPerformed

    private void jPrevBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPrevBtnActionPerformed
        if (currentPageNum > 1) {
            currentPageNum--;
        }
        showList();
    }//GEN-LAST:event_jPrevBtnActionPerformed

    private void jNextBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jNextBtnActionPerformed
        if (currentPageNum < (((showList.size()-1)/5)+1)) {
            currentPageNum++;
        }
        showList();
    }//GEN-LAST:event_jNextBtnActionPerformed

    private void jLastBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLastBtnActionPerformed
        if (currentPageNum <= (((showList.size()-1)/5)+1)) {
            currentPageNum = (((showList.size()-1)/5)+1);
        }
        showList();
    }//GEN-LAST:event_jLastBtnActionPerformed

    private void jDataReadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDataReadBtnActionPerformed
        FileDialog fileDialog = new FileDialog(this, "파일선택", FileDialog.LOAD); 
        fileDialog.setVisible(true);
        String filelocation = fileDialog.getDirectory() + fileDialog.getFile();
        
        try {
            Scanner scn = new Scanner(new File(filelocation));
            String data = "";
            while(true) {
                try {
                    data = scn.nextLine();
                    objectification(data);
                } catch (Exception e) {
                    break;
                }
            }
        } catch (Exception e) {
        }
        showList();
    }//GEN-LAST:event_jDataReadBtnActionPerformed

