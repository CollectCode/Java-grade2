    private void showSearchList()   {
        currentPageNum = 1;
        showList = searchList;
        showList();
    }
    
    private void showList() {
        // jResultPnl 내용초기화    
        this.jResultPnl.removeAll();
        
        // 랭크 초기화
        for (int l = 0; l < showList.size(); l++)  {
            StudentVO rankres = showList.get(l);
            rankres.rank = 1;
        }
        
        // 랭크갱신
        for (int j = 0; j < showList.size(); j++)  {
            for (int k = 0; k < showList.size(); k++)  {
                showList.get(j).setRank(showList.get(k).getAvg());
            }
        }
        
        // 랭크 오름차순으로 sort진행(sort메서드 사용 X)
        for (int m = 0; m < showList.size(); m++)  {
            for (int n = 0; n < showList.size(); n++)   {
                StudentVO temp;
                if (showList.get(m).getRank() < showList.get(n).getRank())  {
                    temp = showList.get(m);
                    showList.set(m, showList.get(n));
                    showList.set(n, temp);
                }
            }
        }
        // jResultPnl에 JButton 생성
        for (int i = (currentPageNum-1)*5; i < (currentPageNum)*5; i++)  {
            StudentVO student = showList.get(i);
            SButton1 obj = new SButton1(student);
            this.jPageNumBtn.setText(currentPageNum +  " / " + (((showList.size()-1)/5)+1));
            this.jResultPnl.add(obj);                                   // 여기서 ResultPnl의 출력은 끝남
            // JButton이 클릭될때 발생하는 기능들 구현
            obj.addActionListener(new java.awt.event.ActionListener() { // 여기부터는 출력된 객체를 클릭했을때 j***Pnl에 값이 출력되도록 구현한 로직
                @Override
                public void actionPerformed(java.awt.event.ActionEvent e) {
                    SButton1 stu = (SButton1)e.getSource();
                    StudentVO obj2 = stu.getStudent();
                    jNameTxt.setText(obj2.getName());
                    jKorTxt.setText("" + obj2.getKor());
                    jEngTxt.setText("" + obj2.getEng());
                    jMathTxt.setText("" + obj2.getMath());
                    jAvgTxt.setText("" + obj2.getAvg());
                    jRankTxt.setText("" + obj2.getRank());
                    clickedStudent = obj2;
                }
            });
        }
        // 위에서 작업한 내용들 전부 출력 후 showList초기화 및 clickedStudent 초기화
        this.pack();
        clickedStudent = null;
    }
