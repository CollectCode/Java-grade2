    private void jDataReadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDataReadBtnActionPerformed
        totalList.clear();
        try {
            BufferedReader br;
            br = new BufferedReader(new FileReader("Scores.dat"));
            String read = "";
            
            while(true) {
                try {
                    read = br.readLine();
                    objectification(read);
                } catch (Exception e) {
                    break;
                }
            }
        } catch (Exception e) {         
        }
        
        showList();
    }//GEN-LAST:event_jDataReadBtnActionPerformed

