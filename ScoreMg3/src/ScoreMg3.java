    private void jDeserializeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeserializeBtnActionPerformed
        totalList.clear();
        
        String filename = "Scores.ser";
        
        try {
            FileInputStream fis = new FileInputStream(filename);
            ObjectInputStream in = new ObjectInputStream(fis);
            
            while(true) {
                try {
                    StudentVO3 deSerializedstu = (StudentVO3)in.readObject();
                    totalList.add(deSerializedstu);
                } catch (Exception e) {
                    break;
                }
            }
            System.out.println("역직렬화 완료");
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("역직렬화 실패");
        }
        
        showList();
    }//GEN-LAST:event_jDeserializeBtnActionPerformed

    private void jDataWriteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDataWriteBtnActionPerformed
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter("Scores.dat", false));
            for (int i = 0; i < totalList.size(); i++)  {
                bw.write(totalList.get(i).getName() + " ");
                bw.write(totalList.get(i).getKor() + " ");
                bw.write(totalList.get(i).getEng() + " ");
                bw.write(totalList.get(i).getMath() + "");
                bw.newLine();
            }
            bw.flush();
            bw.close();
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jDataWriteBtnActionPerformed

