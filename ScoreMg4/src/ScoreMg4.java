    private void showTotalList()    {
        showList = totalList;
        showList();
    }
    
    private void showSearchList()    {
        showList = searchList;
        showList();
    }
    
    private void showList()    {
        this.jResultPnl.removeAll();
        
        for (int i = 0; i < showList.size(); i++)   {
            showList.get(i).rank = 1;
        }
        
        for (int j = 0; j < showList.size(); j++)   {
            for (int k = 0; k < showList.size(); k++)   {
                showList.get(j).setRank(showList.get(k).getAvg());
            }
        }
        
        for (int l = 0; l < showList.size(); l++)   {
            for (int m = 0; m < showList.size(); m++)   {
                if (showList.get(l).getRank() < showList.get(m).getRank())  {
                    StudentVO4 tmp = showList.get(l);
                    showList.set(l, showList.get(m));
                    showList.set(m, tmp);
                }
            }
        }
        try {
            for (int n = (currentPageNum-1)*5; n < currentPageNum*5; n++)   {
                SButton4 obj = new SButton4(showList.get(n));
                this.jResultPnl.add(obj);
                jWhereAmIBtn.setText(currentPageNum + " / " + (((showList.size()-1)/5)+1));
                obj.addActionListener(new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    SButton4 click = (SButton4)evt.getSource();
                    StudentVO4 stu = click.getStudent();
                    jNameTxt.setText(stu.getName());
                    jKorTxt.setText("" + stu.getKor());
                    jEngTxt.setText("" + stu.getEng());
                    jMathTxt.setText("" + stu.getMath());
                    jAvgTxt.setText("" + stu.getAvg());
                    jRankTxt.setText("" + stu.getRank());
                    clickedStudent = stu;
                }
            });
            }
        }
        catch (Exception e) {
        }
        this.pack();
        clickedStudent = null;
    }
    
    private void jInsetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsetBtnActionPerformed
        StudentVO4 obj = new StudentVO4(this.jNameTxt.getText(),
                        Integer.parseInt(this.jKorTxt.getText()),
                        Integer.parseInt(this.jEngTxt.getText()),
                        Integer.parseInt(this.jMathTxt.getText()));
        totalList.add(obj);
        showTotalList();
    }//GEN-LAST:event_jInsetBtnActionPerformed

    private void jUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdateBtnActionPerformed
        clickedStudent.setName(this.jNameTxt.getText());
        clickedStudent.setKor(Integer.parseInt(this.jKorTxt.getText()));
        clickedStudent.setEng(Integer.parseInt(this.jEngTxt.getText()));
        clickedStudent.setMath(Integer.parseInt(this.jMathTxt.getText()));
        clickedStudent.getAvg();
        
        showTotalList();
    }//GEN-LAST:event_jUpdateBtnActionPerformed

    private void jDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteBtnActionPerformed
        if(clickedStudent == null)  {
            return;
        }
        
        totalList.remove(clickedStudent);
        if(showList.size() % 5 == 0)    {
            currentPageNum--;
        }
        showTotalList();
    }//GEN-LAST:event_jDeleteBtnActionPerformed

    private void jSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchBtnActionPerformed
        searchList.clear();
        
        for (int i = 0; i < totalList.size(); i++)  {
            if (totalList.get(i).getName().contains(this.jNameTxt.getText())) {
                searchList.add(totalList.get(i));
            }
        }
        
        showSearchList();
        showList = totalList;
    }//GEN-LAST:event_jSearchBtnActionPerformed
        
    private void objectification(String value)  {
        StudentVO4 obj = new StudentVO4(value);
        totalList.add(obj);
    }
    
