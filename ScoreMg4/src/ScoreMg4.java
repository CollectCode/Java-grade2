    private void jSerializeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSerializeBtnActionPerformed
        String filename = "Scores4.ser";
        
        try 
        {
            FileOutputStream fos = new FileOutputStream(filename);
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            
            for (int i = 0; i < totalList.size(); i++)  {
                oos.writeObject(totalList.get(i));
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jSerializeBtnActionPerformed

    private void jDeserializeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeserializeBtnActionPerformed
        String filename = "Scores4.ser";
        
        try {
            FileInputStream fis = new FileInputStream(filename);
            ObjectInputStream ois = new ObjectInputStream(fis);
            while(true) {
                try {
                    StudentVO4 obj = (StudentVO4)ois.readObject();
                    totalList.add(obj);
                } catch (Exception e) {
                    break;
                }
            }
        } catch (Exception e) {
        }
        showList();
    }//GEN-LAST:event_jDeserializeBtnActionPerformed

